package javabasicsconcepts;

public class StringSubstring {

	public static void main(String[] args) {
		 String s="SourabhaDixit";    
		 System.out.println("Original String: " + s);  
		 System.out.println("Substring starting from index 6: " +s.substring(8));  
		 System.out.println("Substring starting from index 0 to 6: "+s.substring(0,8));   

	}

}
